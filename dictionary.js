// ── 正则模式规则（处理带数值的动态文本）─────────────────
const PATTERN_RULES_ZH_CN = [
  {
    re: /\+(\d+(?:\.\d+)?)\s*to\s*maximum Life/i,
    fn: (m) => `生命上限 +${m[1]}`,
  },
  {
    re: /\+(\d+(?:\.\d+)?)\s*to\s*maximum Mana/i,
    fn: (m) => `魔力上限 +${m[1]}`,
  },
  {
    re: /\+(\d+(?:\.\d+)?)\s*to\s*maximum Energy Shield/i,
    fn: (m) => `能量护盾上限 +${m[1]}`,
  },
  {
    re: /(\d+(?:\.\d+)?)%\s*increased\s*(Fire|Cold|Lightning|Chaos|Physical|Elemental)\s*Damage/i,
    fn: (m) => {
      const map = { Fire:"火焰", Cold:"冰冷", Lightning:"闪电", Chaos:"混沌", Physical:"物理", Elemental:"元素" };
      return `${map[m[2]] ?? m[2]}伤害提高 ${m[1]}%`;
    },
  },
  {
    re: /(\d+(?:\.\d+)?)%\s*(Fire|Cold|Lightning|Chaos|Elemental)\s*Resistance/i,
    fn: (m) => {
      const map = { Fire:"火焰", Cold:"冰冷", Lightning:"闪电", Chaos:"混沌", Elemental:"元素" };
      return `${map[m[2]] ?? m[2]}抗性 +${m[1]}%`;
    },
  },
  {
    re: /Level\s+(\d+)\s+(\w[\w\s]+)/,
    fn: (m) => `${m[2].trim()} Lv.${m[1]}`,
  },
  {
    re: /(\d+(?:\.\d+)?)%\s*increased\s*Movement Speed/i,
    fn: (m) => `移动速度提高 ${m[1]}%`,
  },
  {
    re: /\+(\d+(?:\.\d+)?)\s*to\s*all\s*Attributes/i,
    fn: (m) => `全属性 +${m[1]}`,
  },
];

const PATTERN_RULES_ZH_TW = PATTERN_RULES_ZH_CN.map((rule) => ({
  re: rule.re,
  fn: (m) => rule.fn(m)
    .replace(/护盾/g,  "護盾")
    .replace(/伤害/g,  "傷害")
    .replace(/等级/g,  "等級")
    .replace(/闪电/g,  "閃電")
    .replace(/混沌/g,  "混沌")
    .replace(/元素/g,  "元素")
    .replace(/移动速度/g, "移動速度")
    .replace(/全属性/g,   "全屬性"),
}));

